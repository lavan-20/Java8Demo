
public class MathHelper {

	public static Double findSum(String input1, String input2) {
		Double val1 = Double.valueOf(input1);
		Double val2 = Double.valueOf(input1);
		return val1+val2;
	}

	public static Double findDiff(String input1, String input2) {
		Double val1 = Double.valueOf(input1);
		Double val2 = Double.valueOf(input1);
		return val1-val2;
	}

	public static Double findProduct(String input1, String input2) {
		Double val1 = Double.valueOf(input1);
		Double val2 = Double.valueOf(input1);
		return val1*val2;
	}

	public static Double findQuotient(String input1, String input2) {
		Double val1 = Double.valueOf(input1);
		Double val2 = Double.valueOf(input1);
		return val1/val2;
	}

}
